package com.ir.productions.coachers.entities;

public class Location
{
	private Double lon;

	private Double lat;

	public Double getLon()
	{
		return lon;
	}

	public void setLon(Double lon)
	{
		this.lon = lon;
	}

	public Double getLat()
	{
		return lat;
	}

	public void setLat(Double lat)
	{
		this.lat = lat;
	}
}
